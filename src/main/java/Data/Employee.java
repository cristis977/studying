package Data;

public class Employee {
	String employeeld;
	String isworking;
	
	public Employee(String employeeld, String isworking) {
		super();
		this.employeeld = employeeld;
		this.isworking = isworking;
	}

	public String getEmployeeld() {
		return employeeld;
	}

	public String getIsworking() {
		return isworking;
	}
}
